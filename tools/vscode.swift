3. Git
  1. GitLens: 快速查看代码的编写者
    1. 作用: 
      1. 显示 Git 信息 GitLens 在代码编辑器的每一行显示当前行的 Git 信息，包括作者、提交日期、提交消息等。
      2. 代码注释 GitLens 允许您在代码注释中显示当前行的 Git 信息。
      3. 代码比较 GitLens 可以比较两个提交之间的代码差异，并且可以高亮显示这些差异。
      4. 撤销更改 GitLens 允许您撤销提交或者更改，包括撤销某个文件的更改或者整个分支的更改。
      5. 代码历史记录 GitLens 可以帮助您查看某个文件的历史记录，并且可以在不同的提交之间切换，查看每个提交的详细信息。
      6. 分支管理 GitLens 可以让您查看和管理分支，包括创建、删除、合并等操作。
      7. 代码作者 GitLens 可以帮助您查看每个提交的作者，并且可以根据作者对代码进行过滤
  2. Git History: 查看Git日志 比较分支和提交
  3. Git Graph 插件用于可视化查看存储库的 Git 操作 并从图形中轻松执行Git操作